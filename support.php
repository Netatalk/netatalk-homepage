<h1>Support</h1>


<h3>Bug Reports</h3>
<p>If you think you have found a bug in Netatalk, first see if the bug has already been reported in the <a href="https://github.com/Netatalk/netatalk/issues">Netatalk issue tracker at GitHub</a>.</p>
<p>If no similar bug has been reported yet, please create a new ticket and provide as much context as possible, including Netatalk version, OS version, contents of configuration files, and debug or backtrace logs.</p>
<p>Use the same issue tracker if you want to file a feature request.</p>

<h3>Mailing Lists</h3>
<p>The Netatalk project has two active mailing lists where you can subscribe to ask questions or participate in conversations, as well as search the archives for historical discussion threads.</p>

<ul>
<li>The <a href="https://sourceforge.net/p/netatalk/mailman/netatalk-admins/">netatalk-admins</a> list consists of general discussion of Netatalk. The list is automated
and unmoderated. New releases are announced on this list.</li>
<li>The <a href="https://sourceforge.net/p/netatalk/mailman/netatalk-devel/">netatalk-devel</a> list is the list for developers of Netatalk. If you are interested
in developing Netatalk, you should subscribe to this list.</li>
</ul>

